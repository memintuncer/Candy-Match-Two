

public class MatrixPoint
{
    int MatrixIndexX;
    int MatrixIndexY;

    public MatrixPoint(int x, int y)
    {
        this.MatrixIndexX = x;
        this.MatrixIndexY = y;
    }

    public int GetMatrixIndexX()
    {
        return MatrixIndexX;
    }
    public int GetMatrixIndexY()
    {
        return MatrixIndexY;
    }
}
